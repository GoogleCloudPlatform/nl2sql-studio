# Initialize the metric
qa_quality_metric = PointwiseMetric(
    metric="custom_qa_quality",
    metric_prompt_template=qa_quality_template
)

def run_stage_3_evaluation(final_golden_data, experiment_name="sql-to-nl-eval-run"):

    # Prepare evaluation data
    eval_data = pd.DataFrame(final_golden_data)
    
    # 2. Construct a dedicated context string for the evaluator
    # This guarantees the judge has the exact data it needs to evaluate accuracy
    eval_data['evaluator_context'] = eval_data.apply(
        lambda row: (
            f"""You are an expert natural language generation agent.
        Your persona for this task is: {row['persona']}.
        Persona Description: {get_persona_description(row['persona'])}
        
        Your goal is Reverse Translation: Given the inputs below, generate the precise, pure Natural Language question that would have produced each exact SQL query and result.
        Condition your generation heavily on the ACTUAL result shape to avoid vague or hallucinated intents. (e.g., if the query limits to 5, the question must say 'Which 5...').
        Importantly, craft each question to match your exact persona and refer to the specific Database Schema corresponding to each query.
        
        [CONTEXT]
        - Schema Used: {row['schema']}

        - Query to Translate:
        --- Query ---
        SQL: {row['sql']}
        Result Summary: {row['result_summary']}
        """
        ), 
        axis=1
    )
    
    # 3. Select the correct columns and rename them for Vertex AI
    eval_records = eval_data[['evaluator_context', 'nl_question']]
    eval_records = eval_records.rename(columns={
        'evaluator_context': 'prompt', 
        'nl_question': 'response'
    })
    
    print(eval_records.head()) # Good idea to inspect this before running!
    print(f"\n--- Evaluation Dataset Ready ({len(eval_data)} samples) ---")
    
    # Initialize Vertex AI for Evaluation
    vertexai.init(project="sl-test-project-353312", location="us-central1")

    # Set up EvalTask
    eval_task = EvalTask(
        dataset=eval_records,
        metrics=[qa_quality_metric],
        experiment=experiment_name
    )

    # Execute the LLM judge (omit model since response is included in the dataset)
    print("Evaluating Stage 2 outputs using LLM as a judge...")
    result = eval_task.evaluate()

    # Print out metrics and the LLM's step-by-step reasoning
    print("\n--- Evaluation Summary Metrics ---")
    print(result.summary_metrics)
    
    print("\n--- Detailed Evaluation Table ---")
    print(result.metrics_table)


    return result

def calculate_score_among_complexity(results, golden_data):
    """Calculates average score across each complexity level.
    
    Args:
        results: EvalResult object or pandas DataFrame.
        golden_data: List of dictionaries containing 'complexity' data.
    """
    
    
    if hasattr(results, 'metrics_table'):
        df = results.metrics_table
    elif isinstance(results, pd.DataFrame):
        df = results
    else:
        print("Error: results must be a DataFrame or an EvalResult object")
        return None

    golden_df = pd.DataFrame(golden_data)

    if 'complexity' not in golden_df.columns:
        print("Error: 'complexity' column not found in golden_data")
        return None

    if len(df) == len(golden_df):
        merged_df = df.copy()
        merged_df['complexity'] = golden_df['complexity'].values
    else:
        print(f"Error: Length of results ({len(df)}) and golden_data ({len(golden_df)}) do not match.")
        return None

    score_col = 'custom_qa_quality/score'
    if score_col not in merged_df.columns:
         for col in merged_df.columns:
            if 'score' in col:
                score_col = col
                break
    
    if score_col not in merged_df.columns:
        print("Error: Score column not found.")
        return None

    merged_df[score_col] = pd.to_numeric(merged_df[score_col], errors='coerce')

    avg_scores = merged_df.groupby('complexity')[score_col].mean()

    print("\n--- Average Score by Complexity Level ---")
    print(avg_scores)

    return avg_scores    

